decimal valor = 10536.25m;

// Retornar valor arredondado
System.Console.WriteLine(Math.Round(valor)); // Saída: 10536

// Retornar valor arredondado para cima
System.Console.WriteLine(Math.Ceiling(valor)); // Saída: 10537

// Retornar valor arredondado para baixo
System.Console.WriteLine(Math.Floor(valor)); // Saída: 10536